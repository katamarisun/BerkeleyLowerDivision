def merge(lst1, lst2):
    """Merges two sorted lists.

    >>> merge([1, 3, 5], [2, 4, 6])
    [1, 2, 3, 4, 5, 6]
    >>> merge([], [2, 4, 6])
    [2, 4, 6]
    >>> merge([1, 2, 3], [])
    [1, 2, 3]
    >>> merge([5, 7], [2, 4, 6])
    [2, 4, 5, 6, 7]
    """
    empty = []
    for i in lst1:
        for j in lst2:
            if i > j:
                empty += [j]
                lst2 = [x for x in lst2 if x != j]
        empty += [i]
    empty += lst2
    return empty


